	public void setMemoryBus( MemoryBusIIe memoryBus ) {
		if( memoryBus==null ) {
			memory = new Memory8(0x10000);
			this.memoryBus = new MemoryBusIIe(memory, new byte[0x4000]);
		} else {
			memory = memoryBus.getMemory();
			this.memoryBus = memoryBus;
		}
	}

